def get_last_element(lst):
    # Access and print the last element using negative indexing
    last_element = lst[-1]
    print(last_element)

# Example usage:
sample_list = [1, 2, 3, 4, 5]
get_last_element(sample_list)  # Output: 5

another_list = ['apple', 'banana', 'cherry', 'date']
get_last_element(another_list)  # Output: date
